static inline unsigned long xchg(volatile unsigned int *addr, unsigned int newval)
{
  unsigned int result;
  
  // The + in "+m" denotes a read-modify-write operand.
  asm volatile("lock; xchgl %0, %1" :
               "+m" (*addr), "=a" (result) :
               "1" (newval) :
               "cc");
  return result;
}

